package com.festivalgallery;

public class FestivalGDTO {
	private int fg_num, fg_viewcnt;
	private String fg_title, fg_content, fg_created;
	private String fg_imagename, mem_id;
	
	public int getFg_num() {
		return fg_num;
	}
	public void setFg_num(int fg_num) {
		this.fg_num = fg_num;
	}
	public int getFg_viewcnt() {
		return fg_viewcnt;
	}
	public void setFg_viewcnt(int fg_viewcnt) {
		this.fg_viewcnt = fg_viewcnt;
	}
	public String getFg_title() {
		return fg_title;
	}
	public void setFg_title(String fg_title) {
		this.fg_title = fg_title;
	}
	public String getFg_content() {
		return fg_content;
	}
	public void setFg_content(String fg_content) {
		this.fg_content = fg_content;
	}
	public String getFg_created() {
		return fg_created;
	}
	public void setFg_created(String fg_created) {
		this.fg_created = fg_created;
	}
	public String getFg_imagename() {
		return fg_imagename;
	}
	public void setFg_imagename(String fg_imagename) {
		this.fg_imagename = fg_imagename;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
	
	
}
